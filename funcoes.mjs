function mostrarIdade(nome, idade){
    return `A idade de ${nome} é ${idade}`
}

function mostrarCidade(nome, cidade){
    return `A cidade de ${nome} é ${cidade}`
}

function mostrarHobby(nome, Hobby){
    return `O hobby de ${nome} é ${Hobby}`
}

export{
    mostrarIdade,
    mostrarHobby,
    mostrarCidade
}